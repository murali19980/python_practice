#Python Program to print duplicates from a list of integers
def print_duplicates(lst):
    seen = set()
    duplicates = set()
    for num in lst:
        if num in seen:
            duplicates.add(num)
        else:
            seen.add(num)
    print(duplicates)

lst = [1, 2, 3, 2, 4, 5, 1]
print_duplicates(lst)